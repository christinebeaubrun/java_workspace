public class FindingMaxValue
{
	public static void main(String[] arg)
	{
		int a = Integer.MAX_VALUE;
		System.out.println( "this is a: " + a );
		System.out.println( a + " plus 1 = " + (a+1));
		System.out.println( "2 minus " + a + " = " + (2-1));
		System.out.println( "negative 2 minus " + a + " = " + (-2-a));
		System.out.println( "2 times " + a + " = " + (2*a));
		System.out.println( "4 times " + a + " = " + (4*a));
	}
}
